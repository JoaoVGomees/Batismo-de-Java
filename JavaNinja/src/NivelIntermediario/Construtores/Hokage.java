package NivelIntermediario.Construtores;

public class Hokage {
    String nome;
    int idade;
    boolean vivoOuNao;

    public Hokage() {}

    public Hokage(String nome, int idade, boolean vivoOuNao) {
        this.nome = nome;
        this.idade = idade;
        this.vivoOuNao = vivoOuNao;
    }
}
